#OPERADORES RELACIONES O DE COMPARACION
#Siempre retornan un valor booleano o logico (los resultados posibles son solo True o False)

#MAYOR QUE >
print(5>5)
print(5>=5)

#MENOR  QUE <
print(5<5)
print(5<=5)

#IGUAL QUE == 
#Ojo! un = es asignacion, dos igual == usamos para comparar
print(5==5)

#DISTINTO QUE !=
print(5!=5)

#PARA VER IGUALDADES TAMBIEN PODEMOS USAR EL OPERADOR is
#is se fija si un valor es lo mismo que otro
x= 5
y =5
print(x is y)
#y lo contrario
print(x is not y)